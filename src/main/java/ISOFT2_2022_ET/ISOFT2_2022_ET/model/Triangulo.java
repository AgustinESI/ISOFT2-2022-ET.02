package ISOFT2_2022_ET.ISOFT2_2022_ET.model;

public class Triangulo {
	int lado1;
	int lado2;
	int lado3;
	int angulo1;
	int angulo2;
	int angulo3;

	public Triangulo() {
		super();
	}

	public int getLado1() {
		return lado1;
	}

	public void setLado1(int lado1) {
		this.lado1 = lado1;
	}

	public int getLado2() {
		return lado2;
	}

	public void setLado2(int lado2) {
		this.lado2 = lado2;
	}

	public int getLado3() {
		return lado3;
	}

	public void setLado3(int lado3) {
		this.lado3 = lado3;
	}

	public int getAngulo1() {
		return angulo1;
	}

	public void setAngulo1(int angulo1) {
		this.angulo1 = angulo1;
	}

	public int getAngulo2() {
		return angulo2;
	}

	public void setAngulo2(int angulo2) {
		this.angulo2 = angulo2;
	}

	public int getAngulo3() {
		return angulo3;
	}

	public void setAngulo3(int angulo3) {
		this.angulo3 = angulo3;
	}

}
